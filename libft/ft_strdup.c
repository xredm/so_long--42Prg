#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dest;
	int		i;
	size_t	len;

	len = ft_strlen(s);
	i = 0;
	dest = malloc(sizeof(char) * (len + 1));
	if (!dest)
	{
		return (0);
	}
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
